TRIPLE_CNT NUMBER(10) NOT NULL,
HOMERUN_CNT NUMBER(10) NOT NULL,
CONSTRAINT K PRIMARY KEY(ID,NAME),
CONSTRAINT L FOREIGN KEY(ID) REFERENCES USER_INFO(ID)
